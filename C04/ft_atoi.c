#include <unistd.h>

int	ft_atoi(char *str)
{
	int i;
	int c;

	c = 0;
	i = 0;
	while(str[i] == 32)
		i++;
	while(str[i] == '-' || str[i] == '+'|| (str[i] >= 48 && str[i] <= 57))
	{
		while(str[i] == '-')
		{
				c++;
				i++;
		}
		if(c % 2 != 0)
		{
			write(1, "-", 1);
			c = 0;
		}
		if(str[i] >= 48 && str[i] <= 57)
			write(1, &str[i], 1);
		i++;
	}
	return (0);
}

int	main(void)
{
	char	*a[9];
	
	*a = "  ----- 9841s 20";

	ft_atoi(*a);
}
